    private void btnListadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListadoActionPerformed
        
        //Crea una instancia de la clase DefaultTableModel para construir la tabla que vamos a
        //asociar con el objeto JTable creado en el JFrame
        DefaultTableModel modelo = new DefaultTableModel();
        try{
            //crea un ResulSet para almacenar los datos que necesites de la tabla vuelos
            Statement stmt = (Statement) metodos.conexion.createStatement();
            ResultSet resultado = (ResultSet) stmt.executeQuery("SELECT cod_vuelo, procedencia, destino FROM vuelos");
            
            //A単adimos al modelo de tabla las columnas necesarias. Debe coincidir con las establecidas en
            //la select asociada al ResultSet
            modelo.addColumn("COD_VUELO");
            modelo.addColumn("PROCEDENCIA");
            modelo.addColumn("DESTINO");
            
            //Define un array de objetos para almacenar cada fila de datos
            Object datos[]=new Object[3]; //Numero de columnas de la tabla
            
            // Recorremos la vista resultante de la consulta, la almacenamos en el array de objetos.
            while (resultado.next()){
                for (int i = 0; i < 3; i++) {
                    datos[i]=resultado.getObject(i+1);
                }
                //A単ade la fila al modelo de tabla
                modelo.addRow(datos);
            }
            
            //A単ade el modelo de tabla creado a el objeto JTable creado en el JFrame
            tblRegistroVuelos.setModel(modelo);
            if (stmt != null) {
                stmt.close(); 
            }
        } catch (SQLException ex) {
            System.out.println("Error al acceder a Usuarios");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnListadoActionPerformed

